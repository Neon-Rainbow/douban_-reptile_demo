package model

// Movie 电影信息
type Movie struct {
	// Title 电影标题
	Title string

	// Score 电影评分
	Score string

	// Year 电影年份
	Year string

	// Duration 电影时长
	Duration string

	// Region 电影地区
	Region string

	// Director 导演
	Director string

	// Actors 演员
	Actors string

	// Category 类别
	Category string

	// PosterUrl 海报图片地址
	PosterUrl string

	// MoviePageUrl 电影详情页地址
	MoviePageUrl string
}
